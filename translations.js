// ─────── COMBINE ALL TRANSLATIONS BY LANGUAGE ───────────────────────────────

function buildAllTranslations() {
  const result = {};
  const langs = ['en', 'fr', 'zh', 'de'];
  langs.forEach(function(lang) {
    result[lang] = Object.assign(
      {},
      sharedKeys[lang],
      { main: mainPage[lang] || buildEmpty(mainPage.en, lang) },
      { students: studentsPage[lang] || buildEmpty(studentsPage.en, lang) },
      { parents: parentsPage[lang] || buildEmpty(parentsPage.en, lang) },
      { schools: schoolsPage[lang] || buildEmpty(schoolsPage.en, lang) },
      { findColleges: collegesPage[lang] || buildEmpty(collegesPage.en, lang) },
      { advisors: advisorsPage[lang] || buildEmpty(advisorsPage.en, lang) }
    );
  });
  return result;
}

function buildEmpty(enObj, lang) {
  const result = {};
  Object.keys(enObj).forEach(function(k) { result[k] = lang === 'en' ? enObj[k] : enObj[k]; });
  return result;
}


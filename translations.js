// ─────── BUILD GLOBAL translations OBJECT (backward-compatible fallback) ───
// Some inline code may reference `translations[lang]` directly
const translations = {};
var langs = ['en', 'fr', 'zh', 'de'];
langs.forEach(function(lang) {
  var merged = {};
  Object.keys(sharedKeys[lang] || {}).forEach(function(k) { merged[k] = sharedKeys[lang][k]; });
  var pages = [mainPage, studentsPage, parentsPage, schoolsPage, collegesPage, advisorsPage];
  pages.forEach(function(pageObj) {
    var pageLang = pageObj[lang] || pageObj.en || {};
    Object.keys(pageLang).forEach(function(k) { merged[k] = pageLang[k]; });
  });
  translations[lang] = merged;
});

// ─────── GLOBAL HELPER FUNCTION (page-aware) ────────────────────────────────
function updateLanguage(lang, page) {
  // Get shared keys for this language
  var merged = {};
  Object.keys(sharedKeys.en).forEach(function(k) {
    merged[k] = (sharedKeys[lang] || sharedKeys.en)[k];
  });
  
  // Merge page-specific keys if a page was specified
  if(page && pageLookup[page]) {
    var pageObj = pageLookup[page];
    var pageLang = pageObj[lang] || pageObj.en || {};
    Object.keys(pageLang).forEach(function(k) { merged[k] = pageLang[k]; });
  }
  
  var t = merged;
  if(!t) return;
  
  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    var key = el.dataset.i18n;
    if(t[key]) {
      el.innerHTML = t[key];
    }
  });
  
  if(t.pageTitle) {
    document.title = t.pageTitle;
  }
  document.documentElement.lang = lang;
}